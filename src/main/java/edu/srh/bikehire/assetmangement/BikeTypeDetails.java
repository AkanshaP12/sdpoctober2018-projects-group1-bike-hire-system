package edu.srh.bikehire.assetmangement;

public class BikeTypeDetails {
	private String bikeTypeID;
	private String bikeType;
	private String bikeAgeCategory;
	private String CreationTimeStamp;
	
	public String getBikeTypeID() {
		return bikeTypeID;
	}
	public void setBikeTypeID(String bikeTypeID) {
		this.bikeTypeID = bikeTypeID;
	}
	public String getBikeType() {
		return bikeType;
	}
	public void setBikeType(String bikeType) {
		this.bikeType = bikeType;
	}
	public String getBikeAgeCategory() {
		return bikeAgeCategory;
	}
	public void setBikeAgeCategory(String bikeAgeCategory) {
		this.bikeAgeCategory = bikeAgeCategory;
	}
	public String getCreationTimeStamp() {
		return CreationTimeStamp;
	}
	public void setCreationTimeStamp(String creationTimeStamp) {
		CreationTimeStamp = creationTimeStamp;
	}
	
	

}
